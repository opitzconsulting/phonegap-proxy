define("cordova/exec", function(require, exports, module) {

var cordova = require('cordova'),
    utils = require('cordova/utils'),
    channel = require('cordova/channel');
// Somehow, Faye does not integrate right in the module and build system of
// cordova.js. However, it exposes a global variable, which we use!
require('cordova/plugin/proxy/faye-browser');
var Faye = window.Faye;

var socket;
var config;

function exec(successCallback, failCallback, service, action, actionArgs) {
    var callbackId = null;
    // Register the callbacks and add the callbackId to the positional
    // arguments if given.
    if (successCallback || failCallback) {
        callbackId = service + cordova.callbackId++;
        cordova.callbacks[callbackId] =
            {success:successCallback, fail:failCallback};
    }

    socket.publish('/exec/'+config.channel, {
        callbackId: callbackId,
        service: service,
        action: action,
        actionArgs: actionArgs
    });
}


function connect(_config) {
    config = _config;
    socket = new Faye.Client(config.url, config);
    var disabledFeatures = config.disable;
    if (disabledFeatures) {
        for (var i=0; i<disabledFeatures.length; i++) {
            socket.disable(disabledFeatures[i]);
        }
    }

    socket.subscribe('/callback/'+config.channel, function(response) {
        var args = response.args;

        if (response.success) {
            cordova.callbackSuccess(response.callbackId, args);
        } else {
            cordova.callbackError(response.callbackId, args);
        }
    });

    socket.subscribe('/event/'+config.channel, function(evt) {
        cordova.fireDocumentEvent(evt.type, evt);
    });

    channel.onNativeReady.fire();
}

exec.connect = connect;

module.exports = exec;
});

